    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        if ("".equals(jTextFieldNome.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o nome!");
        } else if ("".equals(jTextFieldUser.getText())) {

            JOptionPane.showMessageDialog(rootPane, "Preencha o usuário!");
        } else if ("".equals(jPasswordField.getText())) {

            JOptionPane.showMessageDialog(rootPane, "Preencha a senha!");
        } else if ("".equals(jTextFieldSalario.getText())) {

            JOptionPane.showMessageDialog(rootPane, "Preencha o salário");
        } else {
            func.setNome(jTextFieldNome.getText());
            func.setUsername(jTextFieldUser.getText());
            func.setPassword(jPasswordField.getText());
            func.setSalario(Float.parseFloat(jTextFieldSalario.getText()));
            control.salvar(func);
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

