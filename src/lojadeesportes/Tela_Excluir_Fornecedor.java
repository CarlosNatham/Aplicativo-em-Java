    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            com.mysql.jdbc.Connection con = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost/java_login_register", "root", "");
            String sql = "SELECT id FROM fornecedor WHERE id=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, jTextFieldID.getText());
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                mod.setId(Integer.parseInt(jTextFieldID.getText()));
                control.excluir(mod);
            } else {
                JOptionPane.showMessageDialog(rootPane, "NÃ£o existe esse ID!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

