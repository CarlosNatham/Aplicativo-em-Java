    private void jButtonpagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonpagamentoActionPerformed
        // TODO add your handling code here:
        if ("".equals(jTextFieldprecoCheque.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o valor do cheque!");
        } else if ("".equals(jTextFieldnomeCheque.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Preencha o seu nome no cheque!");
        } else if ("".equals(jTextFieldcidadeCheque.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Preencha a cidade!");
        } else if ("".equals(jTextFielddataCheque.getText())) {
            JOptionPane.showMessageDialog(rootPane, "Preencha a data!");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Pagamento Efetuado com sucesso");
        }
    }//GEN-LAST:event_jButtonpagamentoActionPerformed

